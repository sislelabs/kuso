// Password drift recovery for native postgres addons.
//
// Background: the kusoaddon helm chart's password-reuse logic
// (lookup the existing conn secret, reuse its password, fallback to
// randAlphaNum) has a known race — if the conn secret is ever
// missing or empty during a reconcile, the chart generates a fresh
// random password while the on-disk pgdata is locked to the
// password from initdb. After that point, services authenticate
// fine (they read POSTGRES_PASSWORD from the env at boot time) but
// the conn secret holds a different value, breaking the SQL console
// and any client that re-reads the secret.
//
// RepairPassword resyncs the database-side password to whatever is
// currently in the conn secret. We can't connect via SQL because
// auth fails — so we exec into the pod as the postgres OS user and
// use psql with `local trust` (which the upstream image enables for
// the unix socket). Then ALTER USER kuso PASSWORD '<conn-secret-pw>'.

package addons

import (
	"bytes"
	"context"
	"fmt"
	"strings"

	corev1 "k8s.io/api/core/v1"
	apierrors "k8s.io/apimachinery/pkg/api/errors"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"k8s.io/client-go/kubernetes/scheme"
	"k8s.io/client-go/tools/remotecommand"
)

// RepairPassword resyncs the postgres user's password inside the
// running addon pod to match the value currently in the addon's
// <name>-conn Secret. Idempotent — running it on a healthy addon
// is harmless (sets the password to itself).
//
// Only supports kind=postgres native addons (not external, not
// useInstanceAddon — those have their own resync paths).
func (s *Service) RepairPassword(ctx context.Context, project, name string) error {
	ns := s.nsFor(ctx, project)
	fqn := addonCRName(project, name)
	addon, err := s.Kube.GetKusoAddon(ctx, ns, fqn)
	if err != nil {
		if apierrors.IsNotFound(err) {
			return fmt.Errorf("%w: addon %s/%s", ErrNotFound, project, name)
		}
		return fmt.Errorf("get addon: %w", err)
	}
	if addon.Spec.Kind != "postgres" {
		return fmt.Errorf("%w: repair-password only supports postgres", ErrInvalid)
	}
	if addon.Spec.External != nil || addon.Spec.UseInstanceAddon != "" {
		return fmt.Errorf("%w: external / instance-shared addons have their own resync paths", ErrInvalid)
	}

	connName := connSecretName(fqn)
	sec, err := s.Kube.Clientset.CoreV1().Secrets(ns).Get(ctx, connName, metav1.GetOptions{})
	if err != nil {
		return fmt.Errorf("read conn secret: %w", err)
	}
	user := string(sec.Data["POSTGRES_USER"])
	pw := string(sec.Data["POSTGRES_PASSWORD"])
	if user == "" || pw == "" {
		return fmt.Errorf("%w: conn secret missing user or password", ErrInvalid)
	}

	// Pod name follows the StatefulSet convention: <release>-0.
	podName := fqn + "-0"
	if _, err := s.Kube.Clientset.CoreV1().Pods(ns).Get(ctx, podName, metav1.GetOptions{}); err != nil {
		return fmt.Errorf("addon pod %s/%s not running: %w", ns, podName, err)
	}

	// We embed the password as a SQL string literal. The conn-secret
	// password is generated by either randAlphaNum (alnum only) or
	// randPassword (hex only) so escaping is trivial — but be paranoid
	// in case a future path lets the user pick.
	escaped := strings.ReplaceAll(pw, "'", "''")
	sqlStmt := fmt.Sprintf(`ALTER USER %q WITH PASSWORD '%s';`, user, escaped)

	stdout, stderr, err := s.execInPod(ctx, ns, podName, "postgres", []string{
		"psql", "-U", user, "-d", "postgres", "-h", "/var/run/postgresql", "-c", sqlStmt,
	})
	if err != nil {
		// Fall back to local socket without -h flag (some images set
		// PGHOST differently). Either should work because the upstream
		// postgres image enables `local trust` for unix-socket auth
		// from the postgres OS user.
		stdout2, stderr2, err2 := s.execInPod(ctx, ns, podName, "postgres", []string{
			"psql", "-U", user, "-d", "postgres", "-c", sqlStmt,
		})
		if err2 != nil {
			return fmt.Errorf("psql alter user failed: stdout=%s stderr=%s err=%w (fallback: stdout=%s stderr=%s err=%v)",
				stdout, stderr, err, stdout2, stderr2, err2)
		}
	}
	return nil
}

// execInPod runs argv in the named container of pod ns/name and
// returns (stdout, stderr, err). No stdin. Used for one-shot admin
// commands like the password resync — not a long-running shell.
func (s *Service) execInPod(ctx context.Context, ns, podName, container string, argv []string) (string, string, error) {
	req := s.Kube.Clientset.CoreV1().RESTClient().
		Post().
		Resource("pods").
		Name(podName).
		Namespace(ns).
		SubResource("exec").
		VersionedParams(&corev1.PodExecOptions{
			Container: container,
			Command:   argv,
			Stdout:    true,
			Stderr:    true,
		}, scheme.ParameterCodec)

	exec, err := remotecommand.NewSPDYExecutor(s.Kube.Config, "POST", req.URL())
	if err != nil {
		return "", "", fmt.Errorf("new executor: %w", err)
	}
	var stdout, stderr bytes.Buffer
	err = exec.StreamWithContext(ctx, remotecommand.StreamOptions{
		Stdout: &stdout,
		Stderr: &stderr,
	})
	return stdout.String(), stderr.String(), err
}
